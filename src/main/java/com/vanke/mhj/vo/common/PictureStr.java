package com.vanke.mhj.vo.common;

public class PictureStr {

	private String bizNo;

	private String imgStrs;

	private String delStrs;

	private Boolean isEnable;

	private Boolean isEmpty;

	public String getBizNo() {
		return bizNo;
	}

	public void setBizNo(String bizNo) {
		this.bizNo = bizNo;
	}

	public String getImgStrs() {
		return imgStrs;
	}

	public void setImgStrs(String imgStrs) {
		this.imgStrs = imgStrs;
	}

	public String getDelStrs() {
		return delStrs;
	}

	public void setDelStrs(String delStrs) {
		this.delStrs = delStrs;
	}

	public Boolean getIsEnable() {
		return isEnable;
	}

	public void setIsEnable(Boolean isEnable) {
		this.isEnable = isEnable;
	}

	public Boolean getIsEmpty() {
		return isEmpty;
	}

	public void setIsEmpty(Boolean isEmpty) {
		this.isEmpty = isEmpty;
	}
}
